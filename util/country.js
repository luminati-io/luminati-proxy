E.mixed2code = function(mixed){
    if (!mixed)
        return;
    if (/^[A-Z]{2}$/.test(mixed))
        return mixed;
    if (mixed.length==2)
        return mixed.toUpperCase();
    for (var ccode in E.list)
    {
        if (E.list[ccode].toLowerCase()==mixed.toLowerCase())
            return ccode;
    }
    if (mixed.length==3)
    {
        for (var acode in alpha_3_list)
        {
            if (alpha_3_list[acode].toLowerCase()==mixed.toLowerCase())
                return acode;
        }
    }
    return mixed;
};

